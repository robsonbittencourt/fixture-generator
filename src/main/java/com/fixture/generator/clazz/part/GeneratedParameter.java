package com.fixture.generator.clazz.part;

public class GeneratedParameter {

	private Class<?> type;
	private String name;

	public GeneratedParameter(Class<?> type, String name) {
		this.type = type;
		this.name = name;
	}

	public Class<?> getType() {
		return type;
	}

	public String getName() {
		return name;
	}
	
}
